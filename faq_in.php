  <font face= "Arial Black">
  <br>
  <h2><u>What is wage theft?</u></h2>
  </div></font>  
  <div class="col-md-8">
  <font face="Arial" size="3.5">
  <br><br>
  <p>Wage theft covers a variety of infractions that occur when workers do not receive their legally or contractually promised wages.</p>
  <p>Common forms of wage theft are:</p>
  <li> Non-payment of overtime </li>
  <li> Not giving workers their last paycheck after a worker leaves a job </li>
  <li> Not paying for all the hours worked </li>
  <li> Not paying minimum wage </li>
  <li> Not paying a worker at all </li>
  </font>
  </div>
  </div>
  
  <div class="col-xs-12">
  <div class="col-md-4">
  <font face= "Arial Black"><br>
  <h2><u>In what types of workplace or industry does wage theft occur?</u></h2>
  </div> </font>  
  <div class="col-md-8"><br><br>
  <font face="Arial" size="3.5">
  <p>Wage theft is endemic, and no group of workers is immune, including workers earning good wages. 
  It is more likely to occur in non-union workplaces.  Union workers generally receive pay according to their negotiated contract, 
  and any wage theft would be challenged by the union. Immigrant and native born workers alike have their wages stolen, 
  though low wage workers are particularly vulnerable.</p>
  <p>Agriculture, poultry processing, janitorial services, restaurant work, garment manufacturing, long term care, 
  home health care and retail are the industries with the most reported cases of wage theft.</p>
  </font>
  </div>
  </div>
  
  <div class="col-xs-12">
  <div class="col-md-4">
  <font face= "Arial Black"><br>
  <h2><u>Where do workers turn if their wages have been stolen?</u></h2>
  </div> </font>  
  <div class="col-md-8"><br><br>
  <font face="Arial" size="3.5">
  <p>There are over 200 worker centers throughout the country, small grassroots organizations that are at the forefront in wage enforcement actions.</p>
  <p>There are two main routes that workers and their advocates use to try to recover wages that were stolen. The first path is direct action-getting
  a group of workers to confront their boss or tell the business' customers what is happening. The second route is through lawsuits, 
  sometimes class action suits against large employers.</p>
  <p>You can find an interactive map listing worker centers as well as other organizations involved in the fight against wage theft here.</p>
  </font>
  </div>
  </div>
  
  <div class="col-xs-12">
  <div class="col-md-4">
  <font face= "Arial Black"><br>
  <h2><u>What kind of solution is needed?</u></h2>
  </div> </font>  
  <div class="col-md-8"><br><br>
  <font face="Arial" size="3.5">
  <p>While many reforms are needed, there are some specific changes that can be made by the new leadership of the DOL, 
  and there are changes that require new legislation by Congress.</p>
  <p>TCase-by-case resolution of individual complaints is not enough. The DOL must engage in targeted investigations of industries and employers where wage theft is rampant,
  in partnership with community organizations and congregations that workers trust, groups that know who the criminal employers are.</p>
  <p>Workers need a consistently proactive, transparent, and accountable Department of Labor Wage and Hour Division.</p>
  </font>
